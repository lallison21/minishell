#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*j;

	j = (char *)s;
	i = 0;
	while (*j)
	{
		j++;
		i++;
	}
	if (*j == '\0' && c == '\0')
		return (j);
	j--;
	while (i != 0)
	{
		if (*j == (char)c)
			return (j);
		i--;
		j--;
	}
	return (NULL);
}
