#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*mem;
	size_t	t;

	t = count * size;
	mem = malloc(t);
	if (mem != NULL)
		ft_bzero(mem, t);
	return (mem);
}
